    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        VentanaBuscarEmpleado v = new VentanaBuscarEmpleado(this, true);
        v.setVisible(true);
        if(v.getAceptado()==1){
            empleado.setId(v.getId());
            gestorEmpleado.recuperar(empleado);
            
            lblcargo.setText(""+empleado.getCategoria().getCategoria());
            lbldni.setText(""+empleado.getDni());
            
            lblnombre.setText(""+empleado.getNombre());

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String usu =  txtusuario.getText();
        String cla =  txtclave.getText();
        usuario.setClave(cla);
        usuario.setEmpleado(empleado);
        usuario.setUsuario(usu);
        gestor.agregar(usuario);
        mostrarusuario();
        limpiar();
                
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int r = JOptionPane.showConfirmDialog(this, "Desea Borrar este Usuario","Confirme",JOptionPane.YES_NO_OPTION);
        if(JOptionPane.YES_OPTION==r){
        int id = Integer.parseInt(tabla.getValueAt(tabla.getSelectedRow(), 0).toString());
        usuario.setId(id);
        gestor.borrar(usuario);
        mostrarusuario();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

