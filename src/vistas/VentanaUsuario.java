    private void mostrarusuario() {
       DefaultTableModel matriz = new DefaultTableModel();
        matriz.addColumn("Id");
        matriz.addColumn("Nombre");
        matriz.addColumn("Usuario");
        matriz.addColumn("Clave");
       
       
        ArrayList<Usuario> lista = gestor.ObtenerLista();
        for (Usuario a : lista) {
            matriz.addRow(new Object[]{ 
                a.getId(),
                a.getEmpleado().getNombre(),
                a.getUsuario(),
                a.getClave(),
          
            });
        }
        tabla.setModel(matriz);    
        
    }

