    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      String clave1= txtclave1.getText();
      String clave2= txtclave2.getText();
      String clave3= txtclave3.getText();
      
      if(!clave1.equals(gestores.GestorUsuario.logeado.getClave()))
      {JOptionPane.showMessageDialog(this, "La clave actual es incorrecta");}
      else{
      if(clave2.isEmpty()||clave3.isEmpty()){JOptionPane.showMessageDialog(this, "La nueva clave no puede ser vacia");}
      else if(!clave2.equals(clave3)){JOptionPane.showMessageDialog(this, "Las nuevas claves deben ser iguales");}
      else {
      gestor.cambiarclave(clave2);
      JOptionPane.showMessageDialog(this, "Su clave fue cambiada exitosamente");
         dispose();
              
      
      }
      }
    }//GEN-LAST:event_jButton2ActionPerformed

