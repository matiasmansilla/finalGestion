    private void mostrarliquidacion() {
 DefaultTableModel matriz = new DefaultTableModel();
        matriz.addColumn("NÂ° liquidacion");
        matriz.addColumn("Fecha");
        matriz.addColumn("Empleado liquidado");
        matriz.addColumn("Categoria");
        
         
        
        
       
        ArrayList<Liquidacion> lista = gestor.ObtenerLista(combodesde.getDate(), combohasta.getDate());
        for (Liquidacion l : lista) {
            matriz.addRow(new Object[]{ 
                l.getId(),
                l.getFecha(),
                l.getEmpleado().getNombre(),
                l.getEmpleado().getCategoria().getCategoria()
                 
            });
         
        }
        tabla.setModel(matriz); 
       
    }

    private void imprimir() {
          ProyectoCiber cn = new ProyectoCiber();
         Map parameters = new HashMap();
           parameters.put("desde",combodesde.getDate());
           parameters.put("hasta",combohasta.getDate());
           
           
            String REPORTE ="C:\\Users\\abel\\Documents\\NetBeansProjects\\ProyectoCiber\\src\\reportes\\ReporteLiquidacion.jrxml";
            
      try{
                JasperReport jasperReport =JasperCompileManager.compileReport(REPORTE);
                
                JasperPrint jasperPrint   =JasperFillManager.fillReport(jasperReport, parameters, cn.getConexion());
                JRViewer jrv=new JRViewer(jasperPrint);
            JDialogImpresion j;
            j = new JDialogImpresion(this,jrv);
           
            j.setVisible(true);
            
            }catch(JRException d){
                d.printStackTrace();
            }
    }


