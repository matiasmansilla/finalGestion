    private void mostrarventas() {
 DefaultTableModel matriz = new DefaultTableModel();
        matriz.addColumn("NÂ° Venta");
        matriz.addColumn("Atendido por");
        matriz.addColumn("Fecha");
        matriz.addColumn("Tipo de Venta");
        matriz.addColumn("Total");
        matriz.addColumn("Iva");
        matriz.addColumn("Precio Final");
        double s=0;
       
        ArrayList<Venta> lista = gestor.ObtenerLista(combodesde.getDate(), combohasta.getDate());
        for (Venta v : lista) {
            matriz.addRow(new Object[]{ 
                v.getId(),
                v.getEmpleado().getNombre(),
                v.getFecha(),
                v.getTipo(),
                v.getTotal(),
                v.getIva(),
                v.getGranTotal()
                
                
            });
            s=s+v.getGranTotal();
        }
        tabla.setModel(matriz);   
        lblTotal.setText("$"+s);
        Grilla.doDate(tabla, 3);
        Grilla.doDouble(tabla, 4);
        Grilla.doDouble(tabla, 5);
        Grilla.doDouble(tabla, 6);
    }

    private void imprimir() {
          ProyectoCiber cn = new ProyectoCiber();
         Map parameters = new HashMap();
           parameters.put("desde",combodesde.getDate());
           parameters.put("hasta",combohasta.getDate());
           parameters.put("total",lblTotal.getText());
           
            String REPORTE ="C:\\Users\\abel\\Documents\\NetBeansProjects\\ProyectoCiber\\src\\reportes\\ReporteVentas.jrxml";
            
      try{
                JasperReport jasperReport =JasperCompileManager.compileReport(REPORTE);
                
                JasperPrint jasperPrint   =JasperFillManager.fillReport(jasperReport, parameters, cn.getConexion());
                JRViewer jrv=new JRViewer(jasperPrint);
            JDialogImpresion j;
            j = new JDialogImpresion(this,jrv);
           
            j.setVisible(true);
            
            }catch(JRException d){
                d.printStackTrace();
            }
    }
