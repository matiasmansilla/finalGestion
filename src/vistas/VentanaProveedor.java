    private void botonagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonagregarActionPerformed
       String razonsocial = txtrazonsocial.getText();
       String cuit = txtcuit.getText();
       String telefono = txttelefono.getText();
       String mail = txtmail.getText();
       String calle = txtcalle.getText();
       String numero = txtnumero.getText();
       String piso = txtpiso.getText();
       String departamento = txtdepartamento.getText();
       int localidad_Id = gestorLocalidad.buscarIdLocalidad(combolocalidad.getSelectedItem().toString());
       
      
       
       Proveedor pro= new Proveedor();
       pro.setRazonSocial(razonsocial);
       pro.setCuit(cuit);
       pro.setTelefono(telefono);
       pro.setMail(mail);
       pro.getDomicilio().setCalle(calle);
       pro.getDomicilio().setNumero(numero);
       pro.getDomicilio().setPiso(piso);
       pro.getDomicilio().setDepartamento(departamento);
       pro.getDomicilio().getLocalidad().setId(localidad_Id);
      
       gestor.agregar(pro);
       mostrarproveedor();
    }//GEN-LAST:event_botonagregarActionPerformed

